<script setup lang="ts">
import { computed } from "vue";
import { kebabCase } from "lodash-es";

import { SubMenu } from "@knime/components";
import type { MenuItem } from "@knime/components";
import DropdownIcon from "@knime/styles/img/icons/arrow-dropdown.svg";

import ToolbarButton from "@/components/common/ToolbarButton.vue";
import { useShortcuts } from "@/plugins/shortcuts";
import type { ShortcutName } from "@/shortcuts";

const $shortcuts = useShortcuts();

interface Props {
  name: ShortcutName;
  withText?: boolean;
  dropdown?: { name: ShortcutName; separator?: boolean }[];
}

const props = withDefaults(defineProps<Props>(), {
  withText: true,
  dropdown: () => [],
});

type MenuItemWithName = MenuItem & { name: ShortcutName; separator?: boolean };

const subMenuItems = computed((): MenuItemWithName[] => {
  if (!props.dropdown || props.dropdown.length === 0) {
    return [];
  }

  const mapShortcutToItem = (item: {
    name: ShortcutName;
    separator?: boolean;
  }) => {
    const { name, separator } = item;
    const shortcut = $shortcuts.get(name);
    const { hotkeyText, icon, title } = shortcut;
    const text = shortcut.text as string;
    const disabled = !$shortcuts.isEnabled(name);

    return {
      name,
      text,
      title,
      disabled,
      hotkeyText,
      icon,
      separator,
    };
  };

  return props.dropdown.map(mapShortcutToItem);
});

const hasSubmenuItems = computed(() => subMenuItems.value.length > 0);

const shortcut = computed(() => $shortcuts.get(props.name));

const title = computed(() => {
  const { title, hotkeyText } = shortcut.value;
  return [title, hotkeyText].filter(Boolean).join(" â€“ ");
});

const enabled = computed(() => $shortcuts.isEnabled(props.name));
</script>

<template>
  <div
    :class="{ 'split-button': hasSubmenuItems }"
    :data-test-id="kebabCase(name)"
  >
    <ToolbarButton
      :class="['toolbar-button', { responsive: !hasSubmenuItems }]"
      :with-text="withText && Boolean(shortcut.text)"
      :disabled="!enabled"
      :title="title"
      :aria-label="shortcut.text"
      @click="$shortcuts.dispatch(name)"
    >
      <Component
        :is="shortcut.icon"
        v-if="shortcut.icon"
        aria-hidden="true"
        focusable="false"
      />
      <span class="text">{{ withText ? shortcut.text : "" }}</span>
    </ToolbarButton>

    <SubMenu
      v-if="hasSubmenuItems"
      ref="submenu"
      :teleport-to-body="false"
      :items="subMenuItems"
      tabindex="-1"
      orientation="left"
      button-title="Open save options"
      :aria-label="`Open submenu for ${shortcut.text}`"
      @item-click="
        (_: MouseEvent, item: MenuItemWithName) =>
          $shortcuts.dispatch(item.name)
      "
      @keydown.enter.stop.prevent="
        (e: KeyboardEvent) => ($refs.submenu as any).toggleMenu(e)
      "
    >
      <DropdownIcon aria-hidden="true" focusable="false" />
    </SubMenu>
  </div>
</template>

